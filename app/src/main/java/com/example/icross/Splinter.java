package com.example.icross;

public class Splinter {
    public float x;
    public float y;
    public float r;
    public int vx;
    public int vy;
    public int color;

    public Splinter(float x, float y, float r, int vx, int vy, int color) {
        super();
        this.x = x;
        this.y = y;
        this.r = r;
        this.vx = vx;
        this.vy = vy;
        this.color = color;
    }
}
